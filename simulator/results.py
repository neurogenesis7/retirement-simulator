"""
Simulation result container.

This object represents a completed simulation run and provides
convenient access to commonly used statistics and summaries.
"""

from __future__ import annotations

from dataclasses import dataclass
import numpy as np

from simulator.assumptions import SimulationConfig
from simulator.statistics import SimulationStatistics
from simulator.timeline import Timeline


@dataclass(slots=True)
class SimulationResults:
    """
    Container for all outputs generated by a simulation.
    """

    config: SimulationConfig
    timeline: Timeline
    paths: np.ndarray
    statistics: SimulationStatistics

    @property
    def summary(self) -> dict:
        """
        Return the summary statistics for the simulation.
        """
        return self.statistics.summary(
            self.timeline.retirement_month
        )

    @property
    def final_distribution(self):
        """
        Ending portfolio values.
        """
        return self.statistics.final_distribution()

    @property
    def final_quantiles(self):
        """
        Ending portfolio quantiles.
        """
        return self.statistics.final_quantiles()

    @property
    def quantiles_over_time(self):
        """
        Portfolio quantiles through time.
        """
        return self.statistics.quantiles_over_time()

    @property
    def survival_curve(self):
        """
        Probability of remaining solvent over time.
        """
        return self.statistics.survival_curve()

    @property
    def success_probability(self):
        """
        Fraction of simulations that do not fail.
        """
        return self.statistics.success_probability()

    @property
    def failure_probability(self):
        """
        Fraction of simulations that fail.
        """
        return self.statistics.failure_probability()